/*
	Модуль signals отвечает за обработку сигналов,
	поступающих в minishell от пользователя или операционной системы.

	Задачи:
	- Установка обработчиков сигналов (signal handlers)
		для интерактивного режима работы.
	- Реакция на SIGINT (Ctrl+C): сброс текущей строки ввода,
		вывод нового prompt, не завершать minishell.
	- Реакция на SIGQUIT (Ctrl+\): игнорировать сигнал в интерактивном режиме.
	- Корректная обработка сигналов в дочерних процессах
		(например, при выполнении внешних команд).
	- Обеспечение корректного завершения minishell по Ctrl+D (EOF).

	Особенности:
	- Вся логика работы с сигналами должна быть изолирована в этом модуле.
	- Для хранения информации о полученном сигнале разрешено использовать
		только одну глобальную переменную (номер сигнала).
	- Модуль должен быть совместим с поведением bash для всех
		поддерживаемых сигналов.
*/
