#include "../../include/lexer/utils.h"

bool	is_space(int c)
{
	return (c == '\f' || c == '\n' || c == '\r' || \
			c == '\t' || c == '\v' || c == ' ');
}

bool	is_operator(int c)
{
	return (c == '|' || c == '&' || c == '(' || c == ')' || c == ';');
}

bool	is_redirection(int c)
{
	return (c == '>' || c == '<');
}
