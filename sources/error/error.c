/*
Модуль error предназначен для централизованной обработки и вывода ошибок в minishell.

Назначение и задачи:

Формирование и вывод сообщений об ошибках для пользователя (например, синтаксические ошибки, ошибки файловых операций, ошибки выполнения команд).
Хранение и установка кода возврата (exit status) при ошибках, чтобы корректно работала переменная $?.
Предоставление функций для других модулей (parser, executor, builtins и др.) для генерации ошибок с единым стилем.
Логирование ошибок (если требуется расширение функционала).
Особенности:

Модуль должен быть изолирован и не содержать логики, связанной с выполнением команд или парсингом.
Все сообщения об ошибках должны быть максимально информативными и соответствовать поведению bash.
Необходимо избегать дублирования кода вывода ошибок в других частях программы.
*/